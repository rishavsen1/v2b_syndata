def esc(s: str) -> str:
    """Escape LaTeX specials in identifiers (underscores, percent, ampersand)."""
    return (str(s).replace("\\", r"\textbackslash{}").replace("_", r"\_")
            .replace("%", r"\%").replace("&", r"\&").replace("#", r"\#"))


def md_table_rows(md: str, section_header: str) -> list[list[str]]:
    """Return the data rows of the first pipe table after `section_header`."""
    idx = md.index(section_header)
    rows: list[list[str]] = []
    in_table = False
    for line in md[idx:].splitlines():
        stripped = line.strip()
        if stripped.startswith("|"):
            cells = [c.strip() for c in stripped.strip("|").split("|")]
            if all(set(c) <= {"-", ":", " "} for c in cells):  # separator
                in_table = True
                continue
            if in_table:
                rows.append(cells)
        elif in_table:
            break
    return rows


