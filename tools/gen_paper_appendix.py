    comp_rows = []
    for i, b in enumerate(campus["buildings"], start=1):
        d, o = b["descriptors"], b["overrides"]
        comp_rows.append(
            f"{i} & {esc(d['building'])} & {esc(d['population'])} & "
            f"{esc(d['equipment'])} & {esc(o.get('pv.pv_type', 'none'))} & "
            f"{esc(o.get('battery.battery_type', 'none'))} & "
            f"{b['weather_profile']} & {o['ev_fleet.ev_count']} & "
            f"{b['seed']} \\\\")

    # -- aggregate the ten batch manifests ------------------------------------ #
    warn_rows, totals = [], {c: 0 for c in WARN_CODES}
    grand_units = grand_warned = grand_warnings = grand_failed = 0
    for i in range(1, 11):
        m = json.loads((CAMPUS_DIR / f"b{i}" / "batch_manifest.json").read_text())
        codes = {c: 0 for c in WARN_CODES}
        warned = n_warn = 0
        for s in m["samples"]:
            per_sample = 0
            for u in (s.get("validation") or {}).get("warned_units", []):
                for w in u["warnings"]:
                    code = re.match(r"([A-Z]\d+)", w).group(1)
                    codes[code] += 1
                    per_sample += 1
