def gen_fits() -> None:
    md = CAL_MD.read_text()

    # -- join the CI CSV with the S1 moments from the markdown table --------- #
    with CIS_CSV.open() as fh:
        cis = {(r["source"], r["region"], r["variable"]): r
               for r in csv.DictReader(fh)}

    s1_md = {}
    for cells in md_table_rows(md, "## S1"):
        src, region, var = cells[0], cells[1].replace(" ", "_"), cells[2].replace(" ", "_")
        s1_md[(src, region, var)] = {
            "src_musig": cells[5], "gen_musig": cells[6], "dmu": cells[7]}

    s1_rows = []
    for key, r in cis.items():
        m = s1_md.get(key)
        if m is None:
            raise SystemExit(f"S1 cell {key} in CSV but not in CALIBRATION_RESULTS.md")
        src, region, var = key
        s1_rows.append(
            f"{esc(src)} & {esc(region.replace('_', ' '))} & "
            f"{var.split('_')[0]} & "
            f"{int(r['n_source']):,} & {int(r['n_generated']):,} & "
            f"{m['src_musig']} & {m['gen_musig']} & {m['dmu']} & "
            f"{float(r['ks_statistic']):.3f} & "
            f"[{float(r['ks_ci_lo']):.3f}, {float(r['ks_ci_hi']):.3f}] & "
            f"{float(r['wasserstein_1']):.2f} & "
            f"[{float(r['w1_ci_lo']):.2f}, {float(r['w1_ci_hi']):.2f}] \\\\")

